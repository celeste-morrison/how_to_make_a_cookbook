# ── Main import loop ───────────────────────────────────────────────────────────

def import_recipes():
    endpoint = f"{API_BASE}/wprm_recipe"
    succeeded = []
    failed = []

    print(f"\n{'─'*60}")
    print(f"  WPRM Recipe Importer")
    print(f"  Target: {WP_URL}")
    print(f"  Recipes to import: {len(RECIPES)}")
    print(f"{'─'*60}\n")

    for i, recipe in enumerate(RECIPES, 1):
        payload = build_wprm_payload(recipe)
        try:
            response = requests.post(
                endpoint,
                json=payload,
                auth=AUTH,
                headers=HEADERS,
                timeout=30,
            )
            if response.status_code in (200, 201):
                recipe_id = response.json().get("id", "?")
                print(f"  ✓ [{i:03d}/{len(RECIPES)}] {recipe['name']} (ID: {recipe_id})")
                succeeded.append(recipe["name"])
            else:
                print(f"  ✗ [{i:03d}/{len(RECIPES)}] {recipe['name']} — HTTP {response.status_code}: {response.text[:120]}")
                failed.append(recipe["name"])
        except requests.exceptions.ConnectionError:
            print(f"  ✗ [{i:03d}/{len(RECIPES)}] {recipe['name']} — Connection refused. Is WordPress running at {WP_URL}?")
            failed.append(recipe["name"])
            break
        except Exception as e:
            print(f"  ✗ [{i:03d}/{len(RECIPES)}] {recipe['name']} — Error: {e}")
            failed.append(recipe["name"])

    print(f"\n{'─'*60}")
    print(f"  Done. {len(succeeded)} succeeded, {len(failed)} failed.")
    if failed:
        print(f"\n  Failed recipes:")
        for name in failed:
            print(f"    - {name}")
    print(f"{'─'*60}\n")


if __name__ == "__main__":
    import_recipes()
